#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	int		c;
	int		a;

	c = 0;
	a = 0;
	while (s1[c])
		c++;
	while (s2[a])
	{
		s1[c] = s2[a];
		c++;
		a++;
	}
	s1[c] = '\0';
	return (s1);
}
