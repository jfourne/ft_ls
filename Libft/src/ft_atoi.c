#include "libft.h"

int		ft_atoi(const char *str)
{
	int	i;
	int neg;
	int res;

	i = 0;
	res = 0;
	neg = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
		neg = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + str[i] - 48;
		i++;
	}
	if (neg == 1)
		res *= -1;
	return (res);
}
